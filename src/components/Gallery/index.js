class Gallery extends Component {
  state = {selectedPhotoId: 0}

  displayingPhoto = id => {
    console.log(id)
    this.setState({selectedPhotoId: id})
  }

  render() {
    const {selectedPhotoId} = this.state
    const selectedPhotoList = imagesList.filter(
      eachPhotoobject => eachPhotoobject.id === selectedPhotoId,
    )
    return (
      <>
        <div className="background-card">
          <img
            src={selectedPhotoList[0].imageUrl}
            alt={selectedPhotoList[0].imageAltText}
            className="displaying-picture-style"
          />
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul className="thumbnail-container">
            {imagesList.map(eachThumbnail => (
              <ThumbnailItem
                displayingPhoto={this.displayingPhoto}
                Thumbnail={eachThumbnail}
                key={eachThumbnail.id}
                isSelected={eachThumbnail.id === selectedPhotoId}
              />
            ))}
          </ul>
        </div>
      </>
    )
  }
}

export default Gallery
